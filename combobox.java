    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int l,i,flag;
        String str,s;
        str=jTextField1.getText();
        l=jComboBox1.getItemCount();
        
        flag=1;
        for(i=0;i<l;i++)
        {
            s=jComboBox1.getItemAt(i).toString();
            if(str.equals(s))
            {
                flag=0;
                break;
            }
        }
        if(flag==1)
            jComboBox1.addItem(str);
        else
            JOptionPane.showMessageDialog(null,"VALUE IS ALREADY PRESENT IN COMBOBOX");
         
    }//GEN-LAST:event_jButton1ActionPerformed

